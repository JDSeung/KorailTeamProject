package com.korail.batch.vo;

public class StationVO {
	private String nodeId;
	private String nodeName;
	private String gyeongbu;
	private String gyeongjeon;       
	private String honam;
	private String jeolla;
	
	public String getNodeId() {
		return nodeId;
	}
	public void setNodeId(String nodeId) {
		this.nodeId = nodeId;
	}
	public String getNodeName() {
		return nodeName;
	}
	public void setNodeName(String nodeName) {
		this.nodeName = nodeName;
	}
	
	public String getGyeongbu() {
		return gyeongbu;
	}
	public void setGyeongbu(String gyeongbu) {
		this.gyeongbu = gyeongbu;
	}
	public String getGyeongjeon() {
		return gyeongjeon;
	}
	public void setGyeongjeon(String gyeongjeon) {
		this.gyeongjeon = gyeongjeon;
	}
	public String getHonam() {
		return honam;
	}
	public void setHonam(String honam) {
		this.honam = honam;
	}
	public String getJeolla() {
		return jeolla;
	}
	public void setJeolla(String jeolla) {
		this.jeolla = jeolla;
	}
	
}
